MAPPINGS.sort(key=lambda x: len(x[0]), reverse=True)
MAP = dict(MAPPINGS)


def rename_html(content: str) -> str:
    def repl(match: re.Match) -> str:
        classes = match.group(1).split()
        return 'class="' + " ".join(MAP.get(c, c) for c in classes) + '"'

    return re.sub(r'class="([^"]*)"', repl, content)


def rename_css(content: str) -> str:
    for old, new in MAPPINGS:
        if old == new:
            continue
        content = re.sub(r"\." + re.escape(old) + r"(?=[\s\.\{,:])", "." + new, content)
    return content


def main() -> None:
    html_path = ROOT / "index.html"
    css_path = ROOT / "style.css"

    html = html_path.read_text(encoding="utf-8")
    css = css_path.read_text(encoding="utf-8")

    html_path.write_text(rename_html(html), encoding="utf-8", newline="\n")
    css_path.write_text(rename_css(css), encoding="utf-8", newline="\n")
    print(f"Renamed {len(MAPPINGS)} class mappings in index.html and style.css")


if __name__ == "__main__":
    main()
